#!/bin/sh
echo "$0 $@"
HCI_DRIVER=`cat /pref/bt.conf | grep -Ev "^#" | grep HCI_DRIVER | cut -c 12-`
if [ "${HCI_DRIVER}" == "ath3k" ]; then
	if [ $1 -ge 15 ]; then
		echo hcitool cmd 0x3f 0x13 0x04 0x0 0x0 0x0 0x4 0x0 0x0 0x0
		hcitool cmd 0x3f 0x13 0x04 0x0 0x0 0x0 0x4 0x0 0x0 0x0
	elif [ $1 -ge 12 ]; then
		echo hcitool cmd 0x3f 0x13 0x04 0x0 0x0 0x0 0x3 0x0 0x0 0x0
		hcitool cmd 0x3f 0x13 0x04 0x0 0x0 0x0 0x3 0x0 0x0 0x0
	elif [ $1 -ge 8 ]; then
		echo hcitool cmd 0x3f 0x13 0x04 0x0 0x0 0x0 0x2 0x0 0x0 0x0
		hcitool cmd 0x3f 0x13 0x04 0x0 0x0 0x0 0x2 0x0 0x0 0x0
	elif [ $1 -ge 4 ]; then
		echo hcitool cmd 0x3f 0x13 0x04 0x0 0x0 0x0 0x1 0x0 0x0 0x0
		hcitool cmd 0x3f 0x13 0x04 0x0 0x0 0x0 0x1 0x0 0x0 0x0
	elif [ $1 -ge 0 ]; then
		echo hcitool cmd 0x3f 0x13 0x04 0x0 0x0 0x0 0x0 0x0 0x0 0x0
		hcitool cmd 0x3f 0x13 0x04 0x0 0x0 0x0 0x0 0x0 0x0 0x0
	fi
fi
